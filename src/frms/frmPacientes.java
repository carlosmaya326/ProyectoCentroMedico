    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        
        if(txtDocumento.getText().equals("") || txtNombre.getText().equals("") || txtApellido.getText().equals("") 
                || txtTelefono.getText().equals("") || txtDireccion.getText().equals("") || txtEPS.getText().equals("") 
                || jcgenero.getSelectedItem().equals("") || jfecha.getDate() == null ){
            
            
            
        }else{
            
            String anio = Integer.toString(jfecha.getCalendar().get(Calendar.YEAR));
            String mes = Integer.toString(jfecha.getCalendar().get(Calendar.MONTH) + 1);
            String dia = Integer.toString(jfecha.getCalendar().get(Calendar.DAY_OF_MONTH));
            String fechaCaducidad = anio + "-" + mes + "-" + dia;
            
            
            if(op == 1){
                
                try{
                    Conexion c = new Conexion();
                    Connection con = c.getConexion();
                    
                    Statement estado = con.createStatement();
                    
                    String insertar = "INSERT INTO Paciente (Documento, Nombres, Apellidos, Telefono, Direccion, EPS, Genero, FechaNacimiento)"
                            + "VALUES ('"+txtDocumento.getText()+"','"+txtNombre.getText()+"','"+txtApellido.getText()+"','"+txtTelefono.getText()+"',"
                            + "'"+txtDireccion.getText()+"','"+jcgenero.getSelectedItem()+"','"+jcgenero.getSelectedItem()+"','"+fechaCaducidad+"')";
                    
                    estado.executeUpdate(insertar);
                    
                    JOptionPane.showMessageDialog(null, "Datos insertados exitosamente");
                    vPacientes p = new vPacientes(user);
                    p.show();
                    dispose();
                    
                }catch(Exception e){
                    System.out.println("error en"+e);
                }
                
                
                
            }
            
            
            
            if(op == 2){
                
                try{
                    
                    Conexion c = new Conexion();
                    Connection con = c.getConexion();
                    Statement estado = con.createStatement();
                    
                    String editar = "UPDATE Paciente SET Nombres = '"+txtNombre.getText()+"' "
                            + ", Apellidos = '"+txtApellido.getText()+"' , Telefono = '"+txtTelefono.getText()+"' "
                            + ", Direccion = '"+txtDireccion.getText()+"', EPS = '"+txtEPS.getText()+"' , Genero = '"+jcgenero.getSelectedItem()+"'"
                            + ", FechaNacimiento = '"+fechaCaducidad+"' WHERE PacienteId = '"+this.pacienteid+"' ";
                    
                    estado.executeUpdate(editar);
                    vPacientes p = new vPacientes(user);
                    JOptionPane.showMessageDialog(null, "Registro actualizado exitosamente");
                    p.show();
                    dispose();
                    
                    
                }catch(Exception e){
                    System.out.println(e.getMessage());
                }
                
                
            }
            
            
            
        }
        
        
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

