    public void Obtenerinfo(){
        
        
        if (opcion.equals("crear")) {
            op = 1;
        }
        
        if(opcion.equals("editar")) {
            
            try{
                op = 2;
                Conexion c = new Conexion();
                Connection con = c.getConexion();
                Statement estado = con.createStatement();
                
                String insertar = "SELECT * FROM paciente WHERE Pacienteid = '"+pacienteid+"'";
                
                ResultSet res = estado.executeQuery(insertar);
                
                if(res.next()){
                    
                 txtDocumento.setText(res.getString("Documento"));
                 txtDocumento.enable(false);
                 txtNombre.setText(res.getString("Nombres"));
                 txtApellido.setText(res.getString("Apellidos"));
                 txtTelefono.setText(res.getString("Telefono"));
                 txtDireccion.setText(res.getString("Direccion"));
                 txtEPS.setText(res.getString("EPS"));
                 jcgenero.setSelectedItem(res.getString("Genero"));
                 jfecha.setDate(res.getDate("FechaNacimiento"));
                 
                }else{
                    System.out.println("No se encontraron los datos");
                }
                
                
            }catch(Exception e){
                System.out.println(e.getMessage());
            }
        }
    }
    

